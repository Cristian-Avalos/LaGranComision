    <div class="hijo3">
      <ol>
        <li>La Divina inspiración verbal de la Biblia y su suprema autoridad en todas las cuestiones de fe y conducta. 
          (1 Tim 3:16, 2 P. 1:19-21).</li>

        <li>La existencia de un solo Dios personal; eterno, uno en esencia, pero existente en tres personas, Padre, 
          Hijo y Espíritu Santo. (1 Jn. 5:7, Mt. 3:16-17, 2 Cor. 13:14).</li>

        <li>Que Jesucristo es el unigénito del Padre. (Jn. 3.16)
            <ol type="a">
              <li>Concebido por el Espíritu Santo. (Lc. 1:35) </li>
              <li>Nacido de la virgen María. (Mt. 1:20)</li>
              <li>Que murió crucificado. (Lc. 23:33)</li>
              <li>Fue sepultado. (Lc. 23:53)</li>
              <li>Resucito de la muerte. (Lc. 24:5-6)</li>
              <li>Ascendió al cielo. (Ef. 4:8) </li>
              <li>Está sentado a la diestra del Padre como nuestro intercesor. (He. 1:3-4, 7:25)</li>
            </ol>
        </li>

        <li>La culpabilidad de hombre, que todos hemos pecado y estamos destituidos de la gloria de Dios (Ro. 3:23, Is. 53:6).</li>

        <li>Que la redención, la justificación, y la regeneración, son obras únicamente por la muerte expiatoria de Cristo, 
          el hijo encarnado de Dios, como nuestro sustituto y la fe de su sangre derramada en el calvario. (Col. 1:14, Ro. 5:1, Tito 5:3).</li>

        <li>La obra del Espíritu Santo proporciona el nuevo nacimiento del creyente arrepentido, efectúa santificación en la vida del 
          pueblo de Dios. (Ro. 6:22).</li>

        <li>La mediación de Jesucristo como único mediador entre Dios y los hombres. (Hch. 4:12, 1 Ti. 2:5).</li>

        <li>La seguridad de la segunda venida del Señor Jesucristo en su cuerpo glorificado.</li>

        <li>En el bautismo en agua por inmersión, en el nombre del Padre, del hijo y del Espíritu Santo. 
          (Mt. 28:19).</li>

        <li>En el bautismo en el Espíritu Santo con evidencia de hablar en otras lenguas. 
          (Hch. 2:4).</li>
        
        <li>En el arrebatamiento de la iglesia para ser recompensada. (1 Ts. 4:17, 2 Cor. 5:10).</li>

        <li>En la sanidad divina como otro de los beneficios del sacrificio de Jesucristo. (Is. 53:4-5, 1 P. 2:24).</li>

        <li>En la iglesia de Dios universal, que consiste en todas las personas que son redimidas por la sangre de Cristo; la cual 
          es el cuerpo visible de Jesucristo en la tierra. (Ap. 5:9; 7:9).</li>

        <li>La resurrección de los muertos, la vida eterna de los salvos y la condenación eterna de los impíos.(Jn. 5:28-29).</li>
        
      </ol>
