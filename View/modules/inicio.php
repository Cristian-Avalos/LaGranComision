    <div id="bien">Quienes Somos</div>

    <div class="hijo">
    La Gran Comisión Cristiana del Evangelio Completo de México A.R, inicia en el año--- en la cd. De Reynosa Tamaulipas,
    siendo el pastor fundador Candelario Guevara, apoyado por nuestro misionero fundador Don Russel.
    Actualmente contamos con ---- iglesias en donde se predica con ahínco el glorioso evangelio que trasforma vidas, 
    la asociación cuenta con  --- ministros debidamente acreditados ante asuntos religiosos. <br><br/>
    Nuestra estructura  está formada por una directiva general –Presidente general y Secretario-Tes. General-, así como, 
    directivos departamentales nacionales: Segadores (varones), Sembradoras (Damas), Linaje Escogido (jóvenes), y Misiones. 
    Contamos con 21 áreas distribuidas en 10 estados de la república mexicana (Baja California (2), Sinaloa (1), Guanajuato (1), 
    San Luis Potosí (3), Veracruz (1), Tabasco- Chiapas (1), Quinta Roo, Nuevo León, Tamaulipas (12); cada área a su vez está 
    constituida por una barra ministerial representada por un presbítero, un (a) presidente de confraternidades y un secretario (a) 
    de área, así como, por directivos departamentales de área.
    <br><br/>
    Ante las autoridades de gobernación contamos con un registro constitutivo SGAR 453/93. Nuestro credo consta de cuatro puntos 
    doctrinales y catorce puntos que constituyen nuestra declaración de fe:
    <br><br/>
